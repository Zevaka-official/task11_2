def str_func(word: str) -> str:
    '''

    :param word: слово
    :return: слово заглавными буквами
    '''
    return word.upper()

def str_func_2(word: str) -> str:
    '''

    :param word: слово
    :return: слово с заглавной буквы
    '''
    return word.title()

def str_func_3(word: str) -> str:
    '''

    :param word: слово
    :return: слово с заглавной буквы
    '''
    return word.capitalize()